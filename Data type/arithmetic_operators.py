#Addition
result = 5 + 3
print(result)  #Output: 8

#Subtraction
result = 7 - 3
print(result)  #Output: 4

#Multiplication
result = 6 * 3
print(result)  #Output: 18

#Division
result = 10 / 3
print(result)  #Output: 3.3333333333333333333

#Floor division
result = 10 // 3
print(result)  #Output: 3

#Modulus
result = 10 % 3
print(result)  #Output: 1

#Exponentiation
result = 5**3
print(result)  #Output: 125